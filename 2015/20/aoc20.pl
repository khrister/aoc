use Data::Dumper;
use Math::Prime::Util qw( divisor_sum );

# Global variables
my $i = 1;

while (1)
{
    my $a =  divisor_sum($i);
    if ($a >= 3310000)
    {
        say "$i  $a";
        exit;
    }
    $i++;
}

